function [x] = cal_xy(A,B,C,D,r1,r2,r3,r4)
x1=A(1);
y1=A(2);
x2=B(1);
y2=B(2);
x3=C(1);
y3=C(2);
x4=D(1);
y4=D(2);
a=[x2-x1,y2-y1;x3-x1,y3-y1;x4-x1,y4-y1];
b=0.5.*[(r1^2-r2^2)+(x2^2+y2^2)-(x1^2+y1^2);(r1^2-r3^2)+(x3^2+y3^2)-(x1^2+y1^2);(r1^2-r4^2)+(x4^2+y4^2)-(x1^2+y1^2)];
x=(a'*a)^(-1)*a'*b;

end

